import json
from sentence_transformers import SentenceTransformer, util

# Load the model
model = SentenceTransformer('all-MiniLM-L6-v2')

input_path = '/content/dataset_for_training.jsonl'
output_path = '/content/dataset_production.jsonl'

records = []
with open(input_path, 'r', encoding='utf-8') as f:
    for line in f:
        if line.strip():
            records.append(json.loads(line))

print(f"Loaded {len(records)} records. Starting aggressive article-body deduplication...")

final_records = []
seen_article_embeddings = []
dropped_count = 0

for record in records:
    # 1. Formatting Guards (Always keep these)
    a2_content_raw = record['messages'][2]['content']
    try:
        a2_data = json.loads(a2_content_raw)
        a2_body = a2_data['agent2_output']['body']
        a2_title = a2_data['agent2_output']['title']
    except:
        dropped_count += 1
        continue

    if "[0]" in a2_body or "Supporting evidence [" in a2_body or "[1]" not in a2_body:
        dropped_count += 1
        continue

    # 2. Aggressive Article Fingerprinting
    # We combine Title + Body to get the full "DNA" of the article
    full_article_text = f"{a2_title} {a2_body}"
    current_embedding = model.encode(full_article_text, convert_to_tensor=True)

    is_duplicate = False
    for seen_emb in seen_article_embeddings:
        # Lowered to 0.80 to catch "rephrased" duplicates like the NIST/WordPress ones
        if util.cos_sim(current_embedding, seen_emb) > 0.80:
            is_duplicate = True
            break

    if not is_duplicate:
        final_records.append(record)
        seen_article_embeddings.append(current_embedding)
    else:
        dropped_count += 1

# Save the truly unique set
with open(output_path, 'w', encoding='utf-8') as f:
    for r in final_records:
        f.write(json.dumps(r) + '\n')

print(f"\nCleanup Complete!")
print(f"✅ Kept {len(final_records)} unique stories.")
print(f"❌ Dropped {dropped_count} total records (formatting errors + duplicates).")

import json
import random
import hashlib
from collections import Counter

# File Paths - Updated for your latest cleaned version
input_file = '/content/dataset_production.jsonl'
train_file = '/content/train.jsonl'
test_file = '/content/test.jsonl'
eval_file = '/content/human_eval_sample_final.md'

# 1. Load the cleaned production data
records = []
try:
    with open(input_file, 'r', encoding='utf-8') as f:
        for line in f:
            if line.strip():
                records.append(json.loads(line))
    print(f"Loaded {len(records)} records from {input_file}.")
except FileNotFoundError:
    print(f"Error: {input_file} not found. Please ensure the file is uploaded to Colab.")
    records = []

if records:
    # 2. Group by Claim Signature for Leak-Proof Splitting
    # This ensures that even if minor rephrasings exist, the same base story won't leak
    grouped_data = {}
    for record in records:
        try:
            agent1_content = json.loads(record['messages'][1]['content'])
            # Create a unique signature based on the claims
            claims = [src.get('extracted_claim', '').strip().lower()
                      for src in agent1_content['research_data']['sources']]
            claims.sort()
            signature = hashlib.md5("|".join(claims).encode('utf-8')).hexdigest()

            if signature not in grouped_data:
                grouped_data[signature] = []
            grouped_data[signature].append(record)
        except (KeyError, IndexError, json.JSONDecodeError):
            # Fallback for any unexpected data issues
            grouped_data[str(random.random())] = [record]

    # 3. Perform 90/10 Train/Test Split
    unique_groups = list(grouped_data.keys())
    random.seed(42) # For reproducible splits
    random.shuffle(unique_groups)

    test_fraction = 0.1
    num_test_groups = max(1, int(len(unique_groups) * test_fraction))
    test_groups = set(unique_groups[:num_test_groups])

    train_records = []
    test_records = []

    for sig, group_records in grouped_data.items():
        if sig in test_groups:
            test_records.extend(group_records)
        else:
            train_records.extend(group_records)

    print(f"\n Safe Split Completed:")
    print(f"  - Training Set: {len(train_records)} records")
    print(f"  - Testing Set:  {len(test_records)} records")

    # Save the splits to new JSONL files
    with open(train_file, 'w', encoding='utf-8') as f:
        for r in train_records: f.write(json.dumps(r) + '\n')
    with open(test_file, 'w', encoding='utf-8') as f:
        for r in test_records: f.write(json.dumps(r) + '\n')

    # 4. Generate Human Eval Sample (Markdown)
    # Sampling from the training set to verify the quality of what the model learns
    sample_size = min(200, len(train_records))
    sample = random.sample(train_records, sample_size)

    with open(eval_file, 'w', encoding='utf-8') as f:
        f.write(f"# Final Human Evaluation Sample ({sample_size} records)\n\n")
        for i, record in enumerate(sample, 1):
            try:
                a1 = json.loads(record['messages'][1]['content'])
                a2 = json.loads(record['messages'][2]['content'])
                claims = [src.get('extracted_claim', '') for src in a1['research_data']['sources']]
                body = a2['agent2_output']['body']

                f.write(f"## Sample {i}\n")
                f.write("### Provided Claims (Agent 1):\n")
                for c in claims:
                    f.write(f"- {c}\n")
                f.write(f"\n### Generated Article (Agent 2 Target):\n{body}\n\n---\n\n")
            except Exception:
                continue

    print(f" Saved final eval sample to {eval_file}")

    # 5. Style Balancing Check
    openings = []
    for r in train_records:
        try:
            body = json.loads(r['messages'][2]['content'])['agent2_output']['body']
            # Grab the first 4 words to detect repetition
            words = body.split()[:4]
            if words:
                openings.append(" ".join(words).lower())
        except Exception:
            continue

    print("\n📊 Style Repetitiveness Analysis (Top 5 Openings):")
    counter = Counter(openings).most_common(5)
    for phrase, count in counter:
        percentage = (count / len(train_records)) * 100
        print(f"  - '{phrase}...' used {count} times ({percentage:.1f}%)")

    if counter and (counter[0][1] / len(train_records)) > 0.20:
        print("\n WARNING: Your model might overfit to the most common opening phrase.")
    else:
        print("\n Style diversity looks excellent for the final training run.")

