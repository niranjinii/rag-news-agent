import json

# Define the paths for Colab's default working directory
input_file = '/content/dataset.jsonl'
output_file = '/content/dataset_cleaned.jsonl'

# The system prompt to anchor the Writer agent persona for Llama
system_message = {
    "role": "system",
    "content": "You are the Writer agent in an automated newsroom. Your task is to synthesize the provided 'research_data' into a journalistic news article. You must strictly output ONLY valid JSON containing the root key 'agent2_output'. Inside 'agent2_output', you must include exactly three keys: 'title' (a compelling headline), 'body' (the article text), and 'used_source_ids' (a list of integer IDs). The 'body' must be objective and include inline citations (e.g., [1], [2]) corresponding to the provided source IDs. Do not hallucinate external facts. Output strictly JSON with no markdown blocks or conversational text."
}

processed_count = 0
error_count = 0

print(f"Reading from {input_file}...")

with open(input_file, 'r', encoding='utf-8') as infile, \
     open(output_file, 'w', encoding='utf-8') as outfile:

    for line_num, line in enumerate(infile, 1):
        if not line.strip():
            continue

        try:
            # 1. Parse the valid JSON line FIRST before altering any quotes.
            # This prevents breaking the JSON structure with unescaped quotes.
            record = json.loads(line)

            # 2. Safely swap smart quotes for single quotes inside the content strings
            if "messages" in record:
                for msg in record["messages"]:
                    if isinstance(msg.get("content"), str):
                        # We use single quotes here so we don't accidentally introduce
                        # unescaped double quotes into the stringified JSON payload.
                        msg["content"] = msg["content"].replace('“', "'").replace('”', "'").replace('‘', "'").replace('’', "'")

                # 3. Add the Llama-optimized system prompt
                record["messages"] = [system_message] + record["messages"]

            # 4. Convert back to a stringified JSON line and save
            outfile.write(json.dumps(record) + '\n')
            processed_count += 1

        except json.JSONDecodeError as e:
            print(f"Skipping line {line_num} due to JSON parse error: {e}")
            error_count += 1

print(f"\nDone! Successfully wrote {processed_count} cleaned records to {output_file}.")
if error_count > 0:
    print(f"Note: {error_count} lines were skipped due to errors.")
else:
    print("All lines processed successfully! You can now download the cleaned dataset from the files sidebar.")

